package example;

public class PullConstant {
	public static void main(String[] args) {
		Integer i1 = 1;
		Integer i2 = 1;
		System.out.println(i1 == i2);
		Double d1 = 1d;
		Double d2 = 1d;
		System.out.println(d1 == d2);
		Long l1 = 127l;
		Long l2 = 127l;
		System.out.println(l1 == l2);
		Float f1 = 50f;
		Float f2 = 50f;
		System.out.println(f1 == f2);
		
	}
}
